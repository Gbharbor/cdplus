#include <cstdio> // Inclui a biblioteca padrão C, que é usada para a função 'printf'.

int main(void) { // Esta é a função principal do programa; 'int' indica que a função retornará um valor inteiro.
    
    // Imprime a string na tela, incluindo novas linhas e espaços.
    printf("Hello\n \t \a world\n"); 
    // '\n' cria uma nova linha na saída.
    // '\t' adiciona um espaço de tabulação (indenta o texto).
    // '\a' produz um som de beep (alerta) no console, se suportado.

    return 0; // Finaliza a função main e retorna 0, indicando que o programa foi concluído com sucesso.
}