// USING PRINTF FOR C
#include <cstdio> // Including the C standard library, which is used for the 'printf' function.

int main(void) { // This is the main function of the program; 'int' indicates that the function will return an integer value.
    
    printf("Hello\n \t \a world\n"); // This is the string that will be displayed on the screen.
    // '\n' creates a new line in the output.
    // '\t' adds a tab space (indentation) in the text.
    // '\a' produces a beep sound (alert) on the console if supported.

    return 0; // This ends the main function and returns 0, indicating that the program finished successfully.
}